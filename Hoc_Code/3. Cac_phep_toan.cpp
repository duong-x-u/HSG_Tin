#include <bits/stdc++.h>
#include <math.h>

using namespace std;

int main() {
    int a = 10;
    int b = 5;

    cout << "a + b = " << a + b << endl;
    cout << "a - b = " << a - b << endl;
    cout << "a * b = " << a * b << endl;
    cout << "a / b = " << a / b << endl;
    cout << "sqrt(a) = " << sqrt(a) << endl;

    return 0;
}
